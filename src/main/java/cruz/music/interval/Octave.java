package cruz.music.interval;

/**
 * @author CruzA
 */
public class Octave
{
	/**
	 * Zero Semitones
	 */
	public static final int ZERO = Semitone.ZERO;

	/**
	 * Twelve Semitones
	 */
	public static final int ONE = Semitone.TWELEVE;

	/** 
	 * Twenty Four Semitones
	 */
	public static final int TWO = Semitone.TWELEVE * 2;

	/** 
	 * Thirty Sixth Semitones
	 */
	public static final int THREE = Semitone.TWELEVE * 3;

	/** 
	 * Forty Eight Semitones
	 */
	public static final int FOUR = Semitone.TWELEVE * 4;

	/** 
	 * Sixty Semitones
	 */
	public static final int FIVE = Semitone.TWELEVE * 5;

	/** 
	 * Seventy Two Semitones
	 */
	public static final int SIX = Semitone.TWELEVE * 6;

	/** 
	 * Eighty Four Semitones
	 */
	public static final int SEVEN = Semitone.TWELEVE * 7;

	/** 
	 * Ninety Six Semitones
	 */
	public static final int EIGHT = Semitone.TWELEVE * 8;

	/**
	 * One Hundred & Eight Semitones
	 */
	public static final int NINE = Semitone.TWELEVE * 9;

	/** 
	 * One Hundred & Twenty Semitones
	 */
	public static final int TEN = Semitone.TWELEVE * 10;

	/**
	 * Negative Twelve Semitones
	 */
	public static final int ONE_DOWN = -ONE;

	/** 
	 * Negative Twenty Four Semitones
	 */
	public static final int TWO_DOWN = -TWO;

	/** 
	 * Negative Thirty Sixth Semitones
	 */
	public static final int THREE_DOWN = -THREE;

	/** 
	 * Negative Forty Eight Semitones
	 */
	public static final int FOUR_DOWN = -FOUR;

	/** 
	 * Negative Sixty Semitones
	 */
	public static final int FIVE_DOWN = -FIVE;

	/** 
	 * Negative Seventy Two Semitones
	 */
	public static final int SIX_DOWN = -SIX;

	/** 
	 * Negative Eighty Four Semitones
	 */
	public static final int SEVEN_DOWN = -SEVEN;

	/** 
	 * Negative Ninety Six Semitones
	 */
	public static final int EIGHT_DOWN = -EIGHT;

	/**
	 * Negative One Hundred & Eight Semitones
	 */
	public static final int NINE_DOWN = -NINE;

	/** 
	 * Negative One Hundred & Twenty Semitones
	 */
	public static final int TEN_DOWN = -TEN;
}
